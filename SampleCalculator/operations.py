
def addition(first, second):
    return first + second

def subtraction(first, second):
    return first - second

def product(first, second):
    return first * second

def floatDivision(first, second):
    return first / second # 5/2 = 2

def integerDivision(first, second):
    return first // second # 5//2 = 2

def modulo(first, second):
    return first % second
